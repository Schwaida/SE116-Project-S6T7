public class Weapon {
    private int weaponDamage;
    private int weaponBlockPower;
    private int weaponRange;
    private int weaponValue;
    private int weaponWeight;

    public void setWeaponDamage(int damage){
        weaponDamage = damage;
    }

    public int getWeaponDamage(){
        return weaponDamage;
    }

    public void setWeaponBlockPower(int blockPower){
        weaponBlockPower = blockPower;
    }

    public int getWeaponBlockPower(){
        return weaponBlockPower;
    }

    public void setWeaponRange(int range){
        weaponRange = range;
    }

    public int getWeaponRange(){
        return weaponRange;
    }

    public void setWeaponValue(int value){
        weaponValue = value;
    }

    public int getWeaponValue(){
        return weaponValue;
    }

    public void setWeaponWeight(int weight){
        weaponWeight = weight;
    }

    public int getWeaponWeight(){
        return weaponWeight;
    }
}
